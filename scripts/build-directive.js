import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const projectRoot = path.resolve(__dirname, '..');
const tempDir = path.join(projectRoot, 'temp_directive');
const targetFile = path.join(projectRoot, 'templates/agent-instructions.md');
const destFile = path.join(projectRoot, 'templates/deepsift-directive.png');

console.log('Generating DeepSift Directive PNG...');

// Clean up old temp dir
if (fs.existsSync(tempDir)) {
    fs.rmSync(tempDir, { recursive: true, force: true });
}
fs.mkdirSync(tempDir, { recursive: true });

try {
    // Run pxpipe to generate the PNG
    execSync(`npx pxpipe export "${targetFile}" --out "${tempDir}"`, { 
        cwd: projectRoot, 
        stdio: 'inherit' 
    });

    // Find the generated file inside temp_directive/pxpipe-export-<hash>/page-001.png
    const dirs = fs.readdirSync(tempDir);
    const exportDir = dirs.find(d => d.startsWith('pxpipe-export-'));
    
    if (!exportDir) {
        throw new Error('Could not find pxpipe-export directory in output.');
    }

    const page1Path = path.join(tempDir, exportDir, 'page-001.png');
    
    if (!fs.existsSync(page1Path)) {
        throw new Error('page-001.png not generated by pxpipe.');
    }

    // Copy to the templates folder
    fs.copyFileSync(page1Path, destFile);
    console.log(`✅ Successfully updated templates/deepsift-directive.png`);

} catch (e) {
    console.error('Failed to generate directive PNG:', e);
    process.exit(1);
} finally {
    // Cleanup temp dir
    if (fs.existsSync(tempDir)) {
        fs.rmSync(tempDir, { recursive: true, force: true });
    }
}
