import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { renderTextToImages } from '../dist/utils/native-renderer.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const projectRoot = path.resolve(__dirname, '..');
const targetFile = path.join(projectRoot, 'templates/agent-instructions.md');
const destFile = path.join(projectRoot, 'templates/deepsift-directive.png');

console.log('Generating DeepSift Directive PNG natively...');

try {
    const content = fs.readFileSync(targetFile, 'utf8');
    const { pages } = renderTextToImages(content, { reflow: true });

    if (pages.length > 0) {
        fs.writeFileSync(destFile, pages[0].png);
        console.log(`✅ Successfully updated templates/deepsift-directive.png natively`);
    } else {
        throw new Error('No pages generated by native renderer.');
    }
} catch (e) {
    console.error('Failed to generate directive PNG natively:', e);
    process.exit(1);
}
